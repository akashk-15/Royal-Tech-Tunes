    private void unumberKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_unumberKeyReleased
        // TODO add your handling code here:
        String un = unumber.getText();
        if (un.matches("^[0-9]*$") && un.length() == 10) {
            unnlabel2.setForeground(Color.green);
            unnlabel2.setText("MOBILE NUMBER IS CORRECT");
            numberflag = 1;
        } else {
            unnlabel2.setForeground(Color.red);
            unnlabel2.setText("MOBILE NUMBER IS INCORRECT");
            numberflag = 0;
        }
    }//GEN-LAST:event_unumberKeyReleased

    private void uemailKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_uemailKeyReleased
        // TODO add your handling code here:
        String s = uemail.getText();
        if (s.matches("^[a-zA-Z0-9+_.-]+@[a-zA-Z0-9.-]+.+(com|in)+$")) {
            uelabel.setForeground(Color.green);
            uelabel.setText("email field is correct");
            mailflag = 1;
        } else {
            uelabel.setForeground(Color.red);
            uelabel.setText("email field is incorrect");
            mailflag = 0;
        }
    }//GEN-LAST:event_uemailKeyReleased

