    private void unameKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_unameKeyReleased
        // TODO add your handling code here:
        String s = uname.getText();

        if (s.matches("^[a-zA-Z]*$")) {
            unlabel.setForeground(Color.green);
            unlabel.setText("NAME FIELD IS CORRECT");
            nameflag = 1;
        } else {
            unlabel.setForeground(Color.red);
            unlabel.setText("NAME FIELD IS INCORRECT");
            nameflag = 0;

        }
    }//GEN-LAST:event_unameKeyReleased

