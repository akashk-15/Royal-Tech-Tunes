    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        // TODO add your handling code here:
        String name = uname.getText();
        String mnumber = unumber.getText();
        String email = uemail.getText();
        String bikename = biname.getText();

        if (name.equals("") || nameflag == 0) {
            JOptionPane.showMessageDialog(null, "NAME FIELDS IS MANDOTORY AND SHOULD BE CORRECT");

        } else if (mnumber.equals("") || numberflag == 0) {
            JOptionPane.showMessageDialog(null, "MOBILE NUMBER IS MANDOTORY AND SHOULD BE CORRECT");

        } else if (email.equals("") || mailflag == 0) {
            JOptionPane.showMessageDialog(null, "EMAIL ID IS MANDOTORY AND SHOULD BE CORRECT");
        } else if (bikename.equals("")) {
            JOptionPane.showMessageDialog(null, "BIKE MODEL NAME IS MANDOTORY ");
        } else {
            JOptionPane.showMessageDialog(null, "all fields are filled thank you");

            DataLogic dl = new DataLogic();
            if (dl.customerDetail(name, mnumber, email, bikename)) {
                JOptionPane.showMessageDialog(null, "Registered successfully");
            } else {

                JOptionPane.showMessageDialog(null, "Registration failed");
            }
            Book bi = new Book();
            bi.unname.setText(uname.getText());
            bi.mnnumber.setText(unumber.getText());
            bi.ID.setText(uemail.getText());
            bi.bike.setText(biname.getText());

            bi.setVisible(true);
            setVisible(false);

        }
    }//GEN-LAST:event_submitActionPerformed

